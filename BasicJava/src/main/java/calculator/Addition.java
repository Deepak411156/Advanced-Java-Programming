public class Addition {
    public static int add(int a, int b)
    {
        int sum = a+b;
        return sum;
    }
    public static int add(int a, int b, int c)
    {
        int sum = a+b+c;
        return sum;
    }
    public static int sub(int x, int y)
    {
        int result= x-y;
        return result;
    }
    
}
